/*Write the java program, which prints the following sequences of values in loops:
24, 18, 12, 6, 0, -6*/

public class Loop_Practice_1
{
 public static void main(String[]args)
 {
  int a=24;
  while (a>=-6)
  {
   if (a>=-6)
   {
    System.out.print(a+","); 
   }
   else if (a==-6)
   {
    System.out.print(a+"."); 
   }
   a-=6;
  }
  }
 }
